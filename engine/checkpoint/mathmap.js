// Legacy family -> { twin otf base name, slot table, italic? }
const FAMILIES = [
  { re: /^cmmi\d+$/, twin: 'latinmodern-math.otf', map: OML },
  { re: /^cmsy\d+$/, twin: 'latinmodern-math.otf', map: OMS },
  // cmex glyphs hang below their reference point; twins need a vertical fix
  { re: /^cmex\d+$/, twin: 'latinmodern-math.otf', map: OMX, omx: true },
  { re: /^cmr(\d+)$/, twin: (m) => `lmroman${normSize(m[1])}-regular.otf`, map: OT1 },
  { re: /^cmbx(\d+)$/, twin: (m) => `lmroman${normSize(m[1])}-bold.otf`, map: OT1 },
  { re: /^cmti(\d+)$/, twin: (m) => `lmroman${normSize(m[1])}-italic.otf`, map: OT1 },
  { re: /^cmsl(\d+)$/, twin: (m) => `lmromanslant${normSize(m[1])}-regular.otf`, map: OT1 },
  { re: /^cmtt(\d+)$/, twin: (m) => `lmmono${normSize(m[1])}-regular.otf`, map: OT1 },
  { re: /^cmss(\d+)$/, twin: (m) => `lmsans${normSize(m[1])}-regular.otf`, map: OT1 },
  { re: /^cmcsc(\d+)$/, twin: () => `lmromancaps10-regular.otf`, map: OT1 },
];

function normSize(s) {
  const n = parseInt(s, 10);
  const sizes = [5, 6, 7, 8, 9, 10, 12, 17];
  let best = 10;
  for (const v of sizes) if (Math.abs(v - n) < Math.abs(best - n)) best = v;
  return best;
}

/**
 * Given a legacy TeX font name (e.g. "cmmi10"), return
 * { twin: otf filename, map: slot->unicode } or null when not a known
 * legacy font (i.e. the font file is already browser-loadable).
 */
export function mapLegacyFont(name) {
  const base = String(name || '').toLowerCase().replace(/\.tfm$/, '');
  for (const fam of FAMILIES) {
    const m = base.match(fam.re);
    if (m) {
      return {
        twin: typeof fam.twin === 'function' ? fam.twin(m) : fam.twin,
        map: fam.map,
        omx: !!fam.omx,
      };
    }
  }
  return null;
}

/**
 * Remap a string of slot codepoints through a slot table. Slots below 32
 * travel PUA-shifted (0xE000+slot) to survive JSON transport.
 */
export function remapText(text, map) {
  let out = '';
  for (const ch of text) {
    let code = ch.codePointAt(0);
    if (code >= 0xe000 && code < 0xe020) code = code - 0xe000;
    const mapped = code < 0x80 && map[code] !== undefined ? map[code] : code;
    out += String.fromCodePoint(mapped);
  }
  return out;
}
