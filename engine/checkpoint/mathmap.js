// mathmap.js — legacy TeX font substitution for browser rendering.
//
// Default lualatex documents typeset TEXT in Latin Modern OpenType (TU
// encoding — browser-renderable), but MATH still uses the classic Computer
// Modern Type1 fonts (cmmi/cmsy/cmex/cmr slots). Browsers cannot load Type1,
// so the instant render tier substitutes each legacy font with its Latin
// Modern OpenType twin and remaps the 7-bit slot to the unicode codepoint of
// the same glyph. Positions and sizes still come from TeX, so layout is
// engine-exact; only the outline source changes (LM was designed as the CM
// successor — shapes match).
//
// Slot tables cover the working set of the OT1/OML/OMS/OMX encodings; glyphs
// outside them fall back to the exact-render tier (PDF chunk).

