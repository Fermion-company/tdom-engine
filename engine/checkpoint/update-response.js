export function buildUpdateResponse({
  rev,
  srcRev,
  editLabel,
  backendName,
  mode,
  modeReasons,
  canonical,
  dirtySource,
  dirtyBlocks,
  depDirty,
  dirtyPages,
  patches,
  timerStats,
  blocks,
  typesetCount,
  forkMs,
  rebooted,
  checkpoints,
  verdict,
  pendingChain,
  reused,
  rebuilt,
  pages,
  changedLabels,
  verifyState,
  fidelity,
  diagnostics,
  engineDiagnostics,
}) {
  return {
    rev,
    srcRev,
    edit: editLabel,
    backend: backendName,
    mode,
    modeReasons,
    canonical,
    dirtySourceNodes: [...dirtySource].map((id) => 'src-' + id),
    dirtySemanticNodes: dirtyBlocks.map((id) => 'blk-' + id),
    dirtyDependencies: depDirty,
    dirtyLayoutNodes: dirtyBlocks.map((id) => 'galley-' + id),
    dirtyPages,
    patches,
    stats: {
      ...timerStats,
      blocksTotal: blocks.length,
      blocksTypeset: typesetCount,
      blocksReparsed: typesetCount,
      semanticCacheHits: blocks.length - typesetCount,
      layoutCacheHits: blocks.length - typesetCount,
      layoutCacheMisses: typesetCount,
      typesetMs: Math.round(forkMs * 100) / 100,
      rebooted,
      checkpoints: checkpoints.size,
      chainVerdict: verdict ?? 'walked',
      chainPending: pendingChain
        ? { kind: pendingChain.kind, from: pendingChain.from }
        : null,
      pagesReused: reused,
      pagesRebuilt: rebuilt,
      pageCount: pages.length,
      macrosChanged: [],
      labelsChanged: [...changedLabels],
      verify: verifyState,
      fidelity,
      diagnostics: [...diagnostics, ...engineDiagnostics.splice(0)],
    },
  };
}

export function buildOpaqueUpdateResponse({
  rev,
  srcRev,
  editLabel,
  backendName,
  mode,
  modeReasons,
  canonical,
  timerStats,
  diagnostics,
}) {
  return {
    rev,
    srcRev,
    edit: editLabel,
    backend: backendName,
    mode,
    modeReasons,
    canonical,
    dirtySourceNodes: [],
    dirtySemanticNodes: [],
    dirtyDependencies: [],
    dirtyLayoutNodes: [],
    dirtyPages: [],
    patches: [],
    stats: {
      ...timerStats,
      blocksTotal: 0,
      blocksTypeset: 0,
      blocksReparsed: 0,
      semanticCacheHits: 0,
      layoutCacheHits: 0,
      layoutCacheMisses: 0,
      typesetMs: 0,
      rebooted: false,
      checkpoints: 0,
      pagesReused: 0,
      pagesRebuilt: 0,
      pageCount: canonical.pageCount,
      macrosChanged: [],
      labelsChanged: [],
      verify: null,
      diagnostics: diagnostics.splice(0),
    },
  };
}
